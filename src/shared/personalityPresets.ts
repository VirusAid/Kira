/** Определить активный пресет по текущей личности (или null = «свой»). */
export function detectPreset(personality: string): string | null {
  const p = personality.trim()
  return PERSONALITY_PRESETS.find((preset) => preset.apply.personality.trim() === p)?.id ?? null
}
